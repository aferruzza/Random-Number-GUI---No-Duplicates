package randomNumbers;
/**
 * Limits
 * Sets up the bounds to generate numbers between.
 * 
 */
public class Limits {
	
	private int lower;
	private int upper;
	
	public Limits() {}
	
	public int getLower() {
		return lower;
	}
	
	public void setLower(int lower) {
		this.lower = lower;
	}
	
	public int getUpper() {
		return upper;
	}
	
	public void setUpper(int upper) {
		this.upper = upper;
	}
	
}